#include "iter.hpp"

void add_one(int &a)
{
    a += 1;
}

int main ()
{
    int arr[5] = {1, 2, 3, 4, 5}; 

    iter(arr, 5, &add_one);

    for (int i = 0; i < 5; i ++)
    {
        std::cout << arr[i] << " " << std::endl;
    }
}

